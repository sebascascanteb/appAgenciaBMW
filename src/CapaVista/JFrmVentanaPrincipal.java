    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        // TODO add your handling code here:
        Usuario_Cliente usuario;  //es el usuario que tiene la sesion iniciada
        jPanInicioSesion jPanInicioSesion = new jPanInicioSesion();
        usuario = jPanInicioSesion.usuarioActivo();

        //Filtro de menus 
        if (usuario != null) {
            //para el inicio de sesion de un Administrador
            if (usuario.getUsuario().getTipoUsuario().equals(TipoUsuario.ADMINISTRADOR)) {
                jMnuMantenimientos.setVisible(true);
                jMnuMantenimientoVehiculo.setVisible(true);
                jMnuMantenimientosPartesVehiculo.setVisible(true);
                jMnuUsuario.setVisible(true);
                jMnuUsuario.setText("Usuario Activo: " + usuario.getUsuario().getCorreo());
                jMnuIngresos.setVisible(false);
                jMnuReportes.setVisible(true);
                jMnuFacturacion.setVisible(true);
                jMnuReporteTotalesFacturados.setVisible(true);
                jMnuReporteClientes.setVisible(true);
                jMnuReporteVehiculo.setVisible(true);
                jMnuReportePartesVehiculo.setVisible(true);
            }

            //para el inicio de sesion de un Vendedor
            if (usuario.getUsuario().getTipoUsuario().equals(TipoUsuario.VENDEDOR)) {
                jMnuMantenimientos.setVisible(true);
                jMnuMantenimientoVehiculo.setVisible(false);
                jMnuMantenimientosPartesVehiculo.setVisible(false);
                jMnuUsuario.setVisible(true);
                jMnuUsuario.setText("Usuario Activo: " + usuario.getUsuario().getCorreo());
                jMnuIngresos.setVisible(false);
                jMnuReportes.setVisible(true);
                jMnuReporteClientes.setVisible(false);
                jMnuReporteTotalesFacturados.setVisible(false);
                jMnuFacturacion.setVisible(true);
            }
            //para el inicio de sesion de un Cliente coorporativo
            if (usuario instanceof ClienteCoorporativo) {
                jMnuUsuario.setVisible(true);
                jMnuUsuario.setText("Usuario Activo: " + usuario.getUsuario().getCorreo());
                jMnuPersonalizacion.setVisible(true);
                jMnuIngresos.setVisible(false);
                jMnuReportes.setVisible(true);
                jMnuReporteClientes.setVisible(false);
                jMnuReporteTotalesFacturados.setVisible(false);
            }
            //para el inicio de sesion de un Cliente personal
            if (usuario instanceof ClientePersonal) {
                jMnuUsuario.setVisible(true);
                jMnuUsuario.setText("Usuario Activo: " + usuario.getUsuario().getCorreo());
                jMnuPersonalizacion.setVisible(true);
                jMnuIngresos.setVisible(false);
                jMnuReportes.setVisible(true);
                jMnuReporteClientes.setVisible(false);
                jMnuReporteTotalesFacturados.setVisible(false);
            }

        }
        
        
    }//GEN-LAST:event_formWindowGainedFocus

