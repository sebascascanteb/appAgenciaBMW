    private void agregarUsuarios() { //usuarios predeterminados
        //limpiar el admin y el vendedor (para que no se repitan los que se crean predeterminadamente)
        try {

            ArrayList<Usuario_Cliente> lista = Usuario_Cliente.listadoUsuario_Clientes();
            for (int i = 0; i < lista.size(); i++) {
                if (lista.get(i).getUsuario().getTipoUsuario().equals(TipoUsuario.ADMINISTRADOR)) {
                    Usuario_Cliente.eliminarUsuario_Cliente(lista.get(i).getUsuario().getCorreo());
                }
                if (lista.get(i).getUsuario().getTipoUsuario().equals(TipoUsuario.VENDEDOR)) {
                    Usuario_Cliente.eliminarUsuario_Cliente(lista.get(i).getUsuario().getCorreo());
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(JFrmVentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        //creacion del admin
        Usuario usuarioAdmin = new Usuario("admin", "123", "Administrador", null, "Local", "00000000", TipoUsuario.ADMINISTRADOR);
        Usuario_Cliente usuario_Cliente_Admin = new Usuario_Cliente(usuarioAdmin, "123456789");
        try {
            Usuario_Cliente.agregarUsuario_Cliente(usuario_Cliente_Admin);
        } catch (Exception ex) {
            Logger.getLogger(JFrmVentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        //creacion de un vendedor
        Usuario usuarioVendedor = new Usuario("vendedor", "123", "Vendedor", null, "Local", "00000000", TipoUsuario.VENDEDOR);
        Usuario_Cliente usuario_Cliente_Vendedor = new Usuario_Cliente(usuarioVendedor, "123456789");
        try {
            Usuario_Cliente.agregarUsuario_Cliente(usuario_Cliente_Vendedor);
        } catch (Exception ex) {
            Logger.getLogger(JFrmVentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    // metodo para la imagen de fondo
    private void agregarFondo(JLabel nombreFondo, String ruta) {
        this.imagen = new ImageIcon(ruta);
        this.icono = new ImageIcon(imagen.getImage().getScaledInstance(
                nombreFondo.getWidth(),
                nombreFondo.getHeight(),
                Image.SCALE_DEFAULT));
        nombreFondo.setIcon(icono);
        repaint();
    }


