    public void llenarComboPartesExtras() {
        jCboPartesExtra.removeAllItems();
        try {
            ArrayList<Partes> listaPartes = Partes.listadoPartes();
            for (Partes partes : listaPartes) {
                jCboPartesExtra.addItem(partes);
            }
            jCboPartesExtra.setSelectedIndex(-1);
        } catch (Exception ex) {
            Logger.getLogger(jPanPersonalizacion.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void llenarComboVehiculos() {
        jCboVehiculos.removeAllItems();
        try {
            ArrayList<Vehiculo> listaVehiculos = Vehiculo.listadoVehiculo();
            for (Vehiculo vehiculo : listaVehiculos) {
                jCboVehiculos.addItem(vehiculo);
            }
            jCboVehiculos.setSelectedIndex(-1);
        } catch (Exception ex) {
            Logger.getLogger(jPanPersonalizacion.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void llenarComboVehiculosFiltro() {
        jCboVehiculos.removeAllItems();
        if (jCheckFosil.isSelected()) {
            tipoVehiculoC = ETipoVehiculo.FOSIL;
        }
        if (jCheckHibrido.isSelected()) {
            tipoVehiculoC = ETipoVehiculo.HIBRIDO;
        }
        if (jCheckElectrico.isSelected()) {
            tipoVehiculoC = ETipoVehiculo.ELECTRICO;
        }
        try {
            ArrayList<Vehiculo> listaVehiculos = Vehiculo.listadoVehiculo();
            for (Vehiculo vehiculo : listaVehiculos) {
                if (vehiculo.geteTipoVehiculo().equals(tipoVehiculoC)) {
                    jCboVehiculos.addItem(vehiculo);
                }
            }
            jCboVehiculos.setSelectedIndex(-1);
        } catch (Exception ex) {
            Logger.getLogger(jPanPersonalizacion.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    

