    private void jBtnAgregarParteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarParteActionPerformed
        // TODO add your handling code here:
        if (jCboVehiculos.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Debe Agregar el Vehiculo primero");
            getToolkit().beep();
            return;
        }
        if (jCboPartesExtra.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar la parte primero");
            getToolkit().beep();
            return;
        }
        parteSeleccionada = (Partes) jCboPartesExtra.getSelectedItem();
        listaPartesSeleccionadas.add(parteSeleccionada);
        jTxaPAgregados.setText("");
        for (Partes partes : listaPartesSeleccionadas) {
            jTxaPAgregados.append("\n" + partes.toString());
            
        }
        txtPrecio.setText(String.valueOf(calculaPrecio()));
    }//GEN-LAST:event_jBtnAgregarParteActionPerformed

